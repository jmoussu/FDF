#include "fdf.h"

void	fill_pixel(unsigned char *str, int x, int y)
{
	int pos;

	if (x >= 1920 || y >= 1080)
		return ;
	if (x < 0 || y < 0)
		return ;
	pos = (4 * x) + (y * 7680);
	str[pos] = 255;
	str[pos + 1] = 255;
	str[pos + 2] = 255;
}
